; ============================================================
; icon_draw: Draw an icon at specified position
; Input: AX = X, BX = Y, SI = icon data pointer
; ============================================================
icon_draw:
    push ax
    push bx
    push cx
    push dx
    push si
    push di
    
    ; Save current position
    mov di, ax          ; DI = X
    mov ax, bx          ; AX = Y
    mov bx, SCREEN_WIDTH
    mul bx              ; AX = Y * 320
    add di, ax          ; DI = Y * 320 + X
    
    ; Copy icon data to video memory
    mov cx, ICON_SIZE / 2    ; 64 bytes (32x32 at 4bpp)
    mov si, [icon_data_ptr]
    
.copy_loop:
    mov al, [si]
    mov [es:di], al
    inc si
    inc di
    loop .copy_loop
    
    pop di
    pop si
    pop dx
    pop cx
    pop bx
    pop ax
    ret

; ============================================================
; icon_draw_df0: Draw DF0 icon at desktop position
; Input: AX = X, BX = Y
; ============================================================
icon_draw_df0:
    push si
    mov si, icon_df0_data
    mov [icon_data_ptr], si
    call icon_draw
    pop si
    ret

